    private void btBuscarConsultasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarConsultasActionPerformed
            if(dcConsultaDia.getDate() == null){
                JOptionPane.showMessageDialog(rootPane, "Insira um data para buscar!", "Atenção!", JOptionPane.WARNING_MESSAGE);
            }else{
                //Seta o TableModel 
                tbConsultas.setModel(getTableModel());
                tbModelConsulta.limpar();
                tbModelConsulta.addListaDeConsultas(criaConsulta());
                if(criaConsulta()!= null){
                    spConsultas.setVisible(true);
                    tbConsultas.setVisible(true);
                    revalidate();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Não há consultas para a data selecionada!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                }

            }
    }//GEN-LAST:event_btBuscarConsultasActionPerformed

