    private void btSelecionaConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSelecionaConsultaActionPerformed
        int indiceLinha = tbConsultas.getSelectedRow();
        //Verifica Linha Selecionada
        if(indiceLinha != -1){
            Voconsultas selectedConsulta = tbModelConsulta.getConsulta(indiceLinha);
            TelaAttConsulta NovaTelaAttConsulta = new TelaAttConsulta(selectedConsulta);
            NovaTelaAttConsulta.setLocationRelativeTo(null);
            NovaTelaAttConsulta.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione ao menos um Paciente!", "Atenção!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btSelecionaConsultaActionPerformed

