    private List<Voconsultas> criaConsulta() {
        SimpleDateFormat dataFormatada= new SimpleDateFormat("dd/MM/yyyy"); //você pode usar outras máscaras
        String novaData = new String();
        novaData = dataFormatada.format(dcConsultaDia.getDate());
        Vodataconsulta dataSelected = CRUD_Vodataconsulta.buscarDataConsulta(novaData);
        List<Voconsultas> ListConsultas = new ArrayList<>();
        Integer idConsulta;
        boolean flagTipo = false;
        for(int i=0; i<20;i++){
            idConsulta = CRUD_Vodataconsulta.getHoraId(i, dataSelected);
            if(idConsulta != 20){
                Voconsultas cons = CRUD_Voconsultas.buscarConsulta(idConsulta);
                if (cons.getTipoConsulta()== 0){
                    if(flagTipo){
                        ListConsultas.add(cons);
                        flagTipo = false;
                    }else{
                        flagTipo = true;
                    }
                }else{
                    ListConsultas.add(cons);
                }
                
                
            }
            
        }
        return ListConsultas;
        
    }

