    private void btRemoverPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverPacienteActionPerformed
        if (achou == null){
            Vopacienteext ext = new Vopacienteext();
            ext.setPacientePessoaIdPessoa(selectedPaciente.getIdPessoa());
            ext.setBebe(rbBebeSim.isSelected());
            ext.setColesterol(rbColesterolSim.isSelected());
            ext.setDiabetes(rbDiabetesSim.isSelected());
            ext.setDoencasCardiacas(rbDoencaCardiacaSim.isSelected());
            ext.setFuma(rbFumanteSim.isSelected());
            if (rbAlergiasSim.isSelected()){
                ext.setAlergias(taAlergias.getText());
            }
            
            if (rbcirurgiasSim.isSelected()){
                ext.setCirurgias(taCirurgias.getText());
            }
            
            //Salva o novo Paciente ext
            boolean salvou = CRUD_Vopacienteext.salvar(ext);
            if (salvou){
                JOptionPane.showConfirmDialog(rootPane, "Dados Atualizadados com Sucesso!");
            }
        }else{
            achou.setPacientePessoaIdPessoa(selectedPaciente.getIdPessoa());
            achou.setBebe(rbBebeSim.isSelected());
            achou.setColesterol(rbColesterolSim.isSelected());
            achou.setDiabetes(rbDiabetesSim.isSelected());
            achou.setDoencasCardiacas(rbDoencaCardiacaSim.isSelected());
            achou.setFuma(rbFumanteSim.isSelected());
            if (rbAlergiasSim.isSelected()){
                achou.setAlergias(taAlergias.getText());
            }
            
            if (rbcirurgiasSim.isSelected()){
                achou.setCirurgias(taCirurgias.getText());
            }
            boolean salvou = CRUD_Vopacienteext.atualizar(achou);
            if (salvou){
                JOptionPane.showMessageDialog(rootPane, "Dados Atualizadados com Sucesso!");
                TelaMedico novaTela = new TelaMedico();
                novaTela.setLocationRelativeTo(null);
                novaTela.setVisible(true);
                this.dispose();
            }
        }
        
    }//GEN-LAST:event_btRemoverPacienteActionPerformed

