    private void btSelectPacienteActionPerformed(java.awt.event.ActionEvent evt) {
        int indiceLinha = tbBuscaPaciente.getSelectedRow();
        selectedPaciente = tbModelPaciente.getPaciente(indiceLinha);
        cmpPaciente.setText(selectedPaciente.getNome());
        cmpRG.setText(selectedPaciente.getRg());
        cmpCPF.setText(selectedPaciente.getCpf());
        dcDataNasc.setDate(selectedPaciente.getDataNascimento());
        cmpTelefone.setText(selectedPaciente.getTelefone());
        cmpCelular.setText(selectedPaciente.getCelular());
        if (selectedPaciente.getTipoConvenio().equals("Plano de Saude")){
            cbTipoConvenio.setSelectedIndex(1);
        }else{
            cbTipoConvenio.setSelectedIndex(0);
        }
        cmpEndereco.setText(selectedPaciente.getEndereco());
        cmpEmail.setText(selectedPaciente.getEmail());
        String id = String.valueOf(selectedPaciente.getIdPessoa());
        cmpID.setText(id);
        popup1.setVisible(false);
        
        //Verifica se há dados adicionais(Vopacienteext)
        
        achou = COPacienteExt.buscaPaciente(selectedPaciente.getIdPessoa());
        if(achou != null){
            if(achou.getBebe()){
                rbBebeSim.setSelected(true);
            }else{
                rbBebeNao.setSelected(true);
            }
            
            if(achou.getColesterol()){
                rbColesterolSim.setSelected(true);
            }else{
                rbColesterolNao.setSelected(true);
            }
            
            if(achou.getDiabetes()){
                rbDiabetesSim.setSelected(true);
            }else{
                rbDiabetesNao.setSelected(true);
            }
            
            if(achou.getDoencasCardiacas()){
                rbDoencaCardiacaSim.setSelected(true);
            }else{
                rbDoencaCardiacaNao.setSelected(true);
            }
            
            if(achou.getFuma()){
                rbFumanteSim.setSelected(true);
            }else{
                rbFumanteNao.setSelected(true);
            }
            
            if(achou.getAlergias() != null){
                rbAlergiasSim.setSelected(true);
                taAlergias.setVisible(true);
                taAlergias.setText(achou.getAlergias());
            }
            
            if (achou.getCirurgias() != null){
                rbcirurgiasSim.setSelected(true);
                taCirurgias.setVisible(true);
                taCirurgias.setText(achou.getCirurgias());
            }
        }
    }
    
    private void btBuscaPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscaPacienteActionPerformed
        tbBuscaPaciente.setModel(getTableModel_NOME());
        tbModelPaciente.limpar();
        tbModelPaciente.addListaDePacientes(criaPaciente_NOME());
        if((tbModelPaciente.getRowCount() == 0) || ("".equals(cmpPaciente.getText()))) {
            JOptionPane.showMessageDialog(rootPane, "Paciente não Encontrada!", "Informação", JOptionPane.INFORMATION_MESSAGE);
        }else{
            
            tbBuscaPaciente.getColumnModel().getColumn(0).setPreferredWidth(25);
            tbBuscaPaciente.getColumnModel().getColumn(1).setPreferredWidth(250);
            SelectPaciente.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                btSelectPacienteActionPerformed(evt);
            }
        });
            SelectPaciente.setText("Selecionar");
            popup1.add(tbBuscaPaciente);
            popup1.add(SelectPaciente);
            popup1.show(btBuscaPaciente, 20, 20);
        }
    }//GEN-LAST:event_btBuscaPacienteActionPerformed

